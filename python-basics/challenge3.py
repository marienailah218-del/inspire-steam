#Program to show for loops of sin, cos and tan of numbers

import math


for x in range(-180,+180,30):
    print(f"syntax of {x} = {math.sin(x)}")

for x in range(-180,+180,30):
    print(f"cosine of {x} = {math.cos(x)}")

for x in range(-180,+180,30):
    print(f"tangent of {x} = {math.tan(x)}")



